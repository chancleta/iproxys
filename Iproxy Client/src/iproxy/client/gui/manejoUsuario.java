    private void addUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addUserActionPerformed
        // TODO add your handling code here:
        crear_user = new crearUsuario(null, rootPaneCheckingEnabled);
        crear_user.setLocationRelativeTo(this);
        crear_user.setVisible(true);
        if (newUser != null) {
            this.setEnabled(false);
            ServerComunication instance = ServerComunication.getInstance();

            if (instance != null) {
                UserManageBeanRemote UserManageBean = instance.UserManageBean();
                if (UserManageBean != null) {

                    if (UserManageBean.Insert(newUser)) {
                        ShowMessages.ShowSucessMessage("Se ha agregado el Usuario con exito", "Iproxys : Mensaje de Notificacion", this);
                        userUnblockModel.removeAllElements();
                        List<User> allUsers = UserManageBean.getAllUsers();
                        for (User Usernew : allUsers) {
                            userUnblockModel.addElement(Usernew.getUsername());
                        }
                    } else {
                        ShowMessages.ShowErrorMessage("No se Pudo agregar el Usuario, Verifique que no se encuentre en la lista", "Iproxys : Mensaje de Notificacion", this);

                    }

                } else {
                    ShowMessages.ShowErrorMessage("No se Pudo Contactar el servidor", "Iproxys : Mensaje de Notificacion", this);

                }
            } else {
                ShowMessages.ShowErrorMessage("No se Pudo Contactar el servidor", "Iproxys : Mensaje de Notificacion", this);

            }
            this.setEnabled(true);
        }
        newUser = null;
    }//GEN-LAST:event_addUserActionPerformed

    private void delUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delUserActionPerformed
        // TODO add your handling code here:
        int selectedIndex = userList.getSelectedIndex();

        if (selectedIndex == -1) {
            ShowMessages.ShowErrorMessage("Se Debe Elegir un Usuario", "Iproxys Error: Imposible Continuar", this);
        } else {
            ServerComunication instance = ServerComunication.getInstance();
            this.setEnabled(false);

            if (instance != null) {
                UserManageBeanRemote UserManageBean = instance.UserManageBean();
                if (UserManageBean != null) {
                    if (UserManageBean.remove((String) userUnblockModel.get(userList.getSelectedIndex()))) {
                        ShowMessages.ShowSucessMessage("Se ha eliminado la ip con exito", "Iproxys : Mensaje de Notificacion", this);
                        userUnblockModel.removeAllElements();
                        List<User> allUsers = UserManageBean.getAllUsers();
                        for (User Usernew : allUsers) {
                            userUnblockModel.addElement(Usernew.getUsername());
                        }
                    } else {
                        ShowMessages.ShowErrorMessage("No se Pudo eliminar la ip", "Iproxys : Mensaje de Notificacion", this);
                    }
                } else {
                    ShowMessages.ShowErrorMessage("No se Pudo Contactar el servidor", "Iproxys : Mensaje de Notificacion", this);

                }
            } else {

                ShowMessages.ShowErrorMessage("No se Pudo Contactar el servidor", "Iproxys : Mensaje de Notificacion", this);

            }
            this.setEnabled(true);

        }
    }//GEN-LAST:event_delUserActionPerformed

