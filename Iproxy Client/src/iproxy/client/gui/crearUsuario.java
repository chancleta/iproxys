    private void saveUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveUserActionPerformed
        // TODO add your handling code here:
        manejoUsuario.newUser = new User();
        manejoUsuario.newUser.setApellido(apellido.getText());
        manejoUsuario.newUser.setCorreo(correo.getText());
        manejoUsuario.newUser.setNombre(nombre.getText());
        manejoUsuario.newUser.setUsername(usuario.getText());
        String password = "";
        
        for(int i = 0; i < clave.getPassword().length ; i++)
        {
            password += clave.getPassword()[i];
        }
        manejoUsuario.newUser.setPassword(password);
        this.dispose();
    }//GEN-LAST:event_saveUserActionPerformed

